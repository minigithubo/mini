package week3_1;
public class BurgerInfo {

	// explanation: A simple class to hold information about a burger including its name, price, and toppings.
	
	String name;
	double price;
	String topping;
	
	BurgerInfo (String name, double price, String topping) {
		this.name = name;
		this.price = price;
		this.topping = topping;
	}	
		  		
}

